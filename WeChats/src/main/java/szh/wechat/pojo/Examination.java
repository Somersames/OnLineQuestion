package szh.wechat.pojo;

public class Examination {
	private int exam_id;
	private String exam_name;
	private String exam_type;
	private String exam_people;
	private int exam_hit;
	private int exam_commenthit;
	public int getExam_id() {
		return exam_id;
	}
	public void setExam_id(int exam_id) {
		this.exam_id = exam_id;
	}
	public String getExam_name() {
		return exam_name;
	}
	public void setExam_name(String exam_name) {
		this.exam_name = exam_name;
	}
	public String getExam_type() {
		return exam_type;
	}
	public void setExam_type(String exam_type) {
		this.exam_type = exam_type;
	}
	public String getExam_people() {
		return exam_people;
	}
	public void setExam_people(String exam_people) {
		this.exam_people = exam_people;
	}
	public int getExam_hit() {
		return exam_hit;
	}
	public void setExam_hit(int exam_hit) {
		this.exam_hit = exam_hit;
	}
	public int getExam_commenthit() {
		return exam_commenthit;
	}
	public void setExam_commenthit(int exam_commenthit) {
		this.exam_commenthit = exam_commenthit;
	}
	
}
